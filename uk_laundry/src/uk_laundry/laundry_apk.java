    private void pilihanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilihanActionPerformed
         if (pilihan.getSelectedItem().equals("Pilihan")){
            txt_harga.setText("0");
        }else if(pilihan.getSelectedItem().equals("Cuci Bersih")){
            txt_harga.setText("3000");
        }else if(pilihan.getSelectedItem().equals("Cuci Kering")){
            txt_harga.setText("4000");
        }else if(pilihan.getSelectedItem().equals("Cuci Setrika")){
            txt_harga.setText("5000");
        }
    }//GEN-LAST:event_pilihanActionPerformed

    private void txt_beratKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_beratKeyReleased
                                 
    if (txt_berat.getText().isEmpty()) {
        txt_totalhar.setText("0");
    } else {
        try {
            int harga = Integer.parseInt(txt_harga.getText());
            float berat = Float.parseFloat(txt_berat.getText());
            Total_Harga = (int) (harga * berat);
            txt_totalhar.setText(String.valueOf(Total_Harga));
        } catch (NumberFormatException e) {
            txt_totalhar.setText("0");
        }
       }
    }//GEN-LAST:event_txt_beratKeyReleased

