    private void transaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transaksiActionPerformed
        if (txt_berat.getText().isEmpty() || txt_harga.getText().isEmpty() || txt_totalhar.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Data harus diisi");
    } else {
        try {
            double bayar = Double.parseDouble(txt_bayar.getText());
            double harga = Double.parseDouble(txt_harga.getText());
            double berat = Double.parseDouble(txt_berat.getText());
            double totalHarga = harga * berat;
            double kembalian = bayar - totalHarga;

            JOptionPane.showMessageDialog(null, "Kembalian Anda :\n" + kembalian);
            reset();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Masukkan angka yang valid.");
        }
         }
    }//GEN-LAST:event_transaksiActionPerformed

